#What does the following error message tell you?

SyntaxError: (irb):2: syntax error, unexpected ')', expecting '}'
  from /usr/local/rvm/rubies/ruby-2.0.0-rc2/bin/irb:16:in `<main>'

  It is saying that there is a (())bracket or ({)curled bracket that has no
  closing pair. Therefore, it is necessary to go back to the code and
  check which bracket is missing its closing pair.
